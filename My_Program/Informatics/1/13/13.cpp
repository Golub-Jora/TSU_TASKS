#include <iostream>
using namespace std;
int main() {                                               //#1    2332 - 2          #1 - 1919 - 1
    int a;                                                 //#2    2332 - 2          #2 - 1919 - 9
    cin >> a;                                                //#3    2332 - 23 - 3     #3 - 1919 - 19 - 9
    cout << ((a % 10) * 10 + ((a / 10) % 10) - a / 100 + 1); //#4    2332 - 233 - 3    #4 - 1919 - 191 - 1
    return 0;                                              //((a/1000)-(a%10))-((a/100%10)-(a/10%10))+1
}